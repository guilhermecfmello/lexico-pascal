

def get_column(c):

    if c >= 'a' and c <= 'z':
        return 0
    elif c >= '0' and c <= '9':
        return 1
    elif c == '.':
        return 2
    elif c == '\'':
        return 3
    elif c == ',':
        return 4
    elif c == ':':
        return 5
    elif c == ')':
        return 6
    elif c == '=':
        return 7
    elif c == '*':
        return 8
    elif c == '[':
        return 9
    elif c == ']':
        return 10
    elif c == '{':
        return 11
    elif c == '}':
        return 12
    elif c == '<':
        return 13
    elif c == '>':
        return 14
    else:
        return -1





